    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // Listar 3

        ArrayList <Mujer> m3 = (ArrayList <Mujer>) m1.clone();
        int n = m3.size();
        for (i = 0; i < n - 1; i++) {
            for (j = 0; j < n - i - 1; j++) {
                int edad1 = m3.get(j).getEdad();
                int edad2 = m3.get(j + 1).getEdad();

                if (edad1 > edad2 ) {
                    // Intercambiar
                    Mujer ayuda = m3.get(j);
                    m3.set(j, m3.get(j + 1));
                    m3.set(j + 1, ayuda);
                }
            }
        }
        // imprimir
        conta = 1;
        for (i = 0; i < m3.size(); i++){
            System.out.println("Participante " + conta + ":" + m3.get(i).toString());
            conta++;
        }
    }//GEN-LAST:event_jButton8ActionPerformed

