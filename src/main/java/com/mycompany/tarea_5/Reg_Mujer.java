    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // LISTAR 2
        if (m1.isEmpty()){
            JOptionPane.showMessageDialog(null, "No hay participantes femeninos registrados", "Aviso", JOptionPane.ERROR_MESSAGE);
        }
        else{
            JOptionPane.showMessageDialog(null, "A continuación se mostrará la información compacta de " + m1.size() + " participante(s)", "AVISO", JOptionPane.INFORMATION_MESSAGE);
            //Saber como quiere que los muestre
            do{
                opc = JOptionPane.showInputDialog(null, "Escriba 1 si desea mostrarlos como están registrados"
                    + "\nEscriba 2 si desea mostrarlos ordenados por apellido paterno (alfabeticamente)", "MOSTRAR", JOptionPane.QUESTION_MESSAGE );

                if(!"1".equals(opc) && !"2".equals(opc) && opc != null){
                    JOptionPane.showMessageDialog(null, "Opción Inválida, vuelva a escribir", "ERROR", JOptionPane.ERROR_MESSAGE);
                } else if (opc == null){
                    JOptionPane.showMessageDialog(null, "OPERACIÓN CANCELADA", "AVISO", JOptionPane.WARNING_MESSAGE);
                }

            }while(!"1".equals(opc) && !"2".equals(opc) && opc != null);

            // Mostrar según sea el caso

            conta = 1;
            switch (opc){
                case "1":
                for (i = 0; i < m1.size(); i++){
                    System.out.println("Participante " + conta + ":" + m1.get(i).toString());
                    conta++;
                }
                break;
                case "2":
                //Algoritmo burbuja
                //1.- Hacer copia del arreglo original para no modificarlo
                ArrayList <Mujer> m2 = (ArrayList <Mujer>) m1.clone();
                //2.- Algoritmo burbuja
                int n = m2.size();
                for (i = 0; i < n - 1; i++) {
                    for (j = 0; j < n - i - 1; j++) {
                        String apellido1 = m2.get(j).getPaterno();
                        String apellido2 = m2.get(j + 1).getPaterno();

                        if (apellido1.compareToIgnoreCase(apellido2) > 0) {
                            // Intercambiar
                            Mujer ayuda = m2.get(j);
                            m2.set(j, m2.get(j + 1));
                            m2.set(j + 1, ayuda);
                        }
                    }
                }
                // imprimir
                for (i = 0; i < m2.size(); i++){
                    System.out.println("Participante " + conta + ":" + m2.get(i).toString());
                    conta++;
                }

                break;

                case null:
                break;
                default: JOptionPane.showMessageDialog(null, "ERROR", "ERROR", JOptionPane.ERROR_MESSAGE);
            }

        }
    }//GEN-LAST:event_jButton3ActionPerformed

