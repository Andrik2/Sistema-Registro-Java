    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //BOTÓN DE REGISTRO
        String nombre = jTextField1.getText();
        String paterno = jTextField2.getText();
        String materno = jTextField3.getText();
        String edad0 = jTextField4.getText();
        int edad = Integer.valueOf(edad0);
        String facultad = jTextField5.getText();

        int opcion = JOptionPane.showConfirmDialog(null, "¿Es correcta la siguiente información?\n"
            + "Nombre: " + nombre
            + "\nPaterno: " + paterno
            + "\nMaterno: " + materno
            + "\nEdad: " + edad
            + "\nFacultad: " + facultad
            , "CONFIRMACIÓN DE DATOS", JOptionPane.YES_NO_OPTION);

        if (opcion == 0){
            Mujer muj = new Mujer();
            muj.setNombre(nombre);
            muj.setPaterno(paterno);
            muj.setMaterno(materno);
            muj.setEdad(edad);
            muj.setFacultad(facultad);

            if (m1.isEmpty()){
                id++;
            }
            else{
                id = m1.getLast().getId() + 1;
            }

            //Preseleccionar el numero de 0's
            if (id < 10){
                tipo = "M-000"+id;
            } else if (id < 100){
                tipo = "M-00"+id;
            } else if (id < 1000){
                tipo = "M-0"+id;
            } else{
                tipo = "M-"+id;
            }
            
            muj.setId(id);
            muj.setTipo(tipo);

            m1.add(muj);

            JOptionPane.showMessageDialog(null, "Gracias por confirmar al participante " + nombre
                    + ", su número de participante es: " + tipo + "\nRegistro completado", "STATUS", JOptionPane.INFORMATION_MESSAGE);

        }
        else if (opcion == 1){
            JOptionPane.showMessageDialog(null, "DATOS INCORRECTOS. \nFavor de volver a ingresar", "STATUS", JOptionPane.WARNING_MESSAGE);
        }
        else{
            JOptionPane.showMessageDialog(null, "REGISTRO CANCELADO", "STATUS", JOptionPane.ERROR_MESSAGE);
        }

        //Limpiar entradas
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

